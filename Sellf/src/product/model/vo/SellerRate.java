package product.model.vo;

public class SellerRate {
	private int totalCount;
	private double avr;
	
	public int getTotalCount() {
		return totalCount;
	}
	public void setTotalCount(int totalScore) {
		this.totalCount = totalScore;
	}
	public double getAvr() {
		return avr;
	}
	public void setAvr(double d) {
		this.avr = d;
	}
	
}
